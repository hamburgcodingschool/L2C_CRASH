// ARITHMETIC OPERATORS
//  +    -     *     /      %

var a = 17;
var b = 8;

var sum = a + b;
var subt = a - b;
var mult = a * b;
var divi = a / b;
var remainder = a % b;

console.log("The sum of " + a + " and " + b + " is " + sum);
console.log("The difference between " + a + " and " + b + " is " + subt);
console.log("The multiplication of " + a + " and " + b + " is " + mult);
console.log("The division of " + a + " by " + b + " is " + divi);
console.log("The remainder of " + a + " by " + b + " is " + remainder);